//Swapping of two numbers.
package basic;

public class Swap {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 4;
        int num3 = num1;
        num1 = num2;
        num2 = num3;
        System.out.println("1st number is: "+ num1);
        System.out.println("2nd number is: "+ num2);
        

    }
}
